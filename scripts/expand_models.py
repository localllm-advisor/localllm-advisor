with open("public/data/models.json") as f:
    existing = json.load(f)

existing_ids = {m["id"] for m in existing}
added = 0
for m in NEW_MODELS:
    if m["id"] not in existing_ids:
        existing.append(m)
        added += 1
    else:
        print(f"  SKIP (duplicate): {m['id']}")

# Sort by family then params_b
existing.sort(key=lambda m: (m["family"], m["params_b"]))

with open("public/data/models.json", "w") as f:
    json.dump(existing, f, indent=2)

print(f"\nDone! Added {added} new models.")
print(f"Total models: {len(existing)}")

# Summary by family
families = {}
for m in existing:
    fam = m.get("family", "unknown")
    families[fam] = families.get(fam, 0) + 1
for fam, count in sorted(families.items(), key=lambda x: -x[1]):
    print(f"  {fam}: {count}")
