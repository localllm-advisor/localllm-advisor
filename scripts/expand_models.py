import json, math

def vram(params_b, bpw):
    """Estimate VRAM in MB: params * bpw * 128 bytes + overhead."""
    base = round(params_b * bpw * 128)
    if params_b < 1:     overhead = 300
    elif params_b < 4:   overhead = 450
    elif params_b < 15:  overhead = 500
    elif params_b < 35:  overhead = 800
    elif params_b < 80:  overhead = 1500
    else:                overhead = 3000
    return base + overhead

def quants(params_b, ollama_base=None):
    levels = [
        ("Q4_K_M", 4.5,  0.94),
        ("Q6_K",   6.5,  0.97),
        ("Q8_0",   8.0,  0.995),
        ("FP16",   16.0, 1.0),
    ]
    result = []
    for level, bpw, quality in levels:
        entry = {
            "level": level,
            "bpw": bpw,
            "vram_mb": vram(params_b, bpw),
            "quality": quality,
        }
        if ollama_base:
            suffix = level.lower().replace("_", "_")
            entry["ollama_tag"] = f"{ollama_base}:{level.lower()}"
        result.append(entry)
    return result

